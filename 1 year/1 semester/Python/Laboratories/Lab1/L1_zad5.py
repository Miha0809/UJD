# **
def pow(num1, num2):
    return num1 ** num2


# *
def multiplication(num1, num2):
    return num1 * num2


# /
def division(num1, num2):
    return num1 / num2


# +
def suma(num1, num2):
    return num1 + num2


# -
def minus(num1, num2):
    return num1 - num2


# //
def whole_part(num1, num2):
    return num1 // num2


# %
def remainder(num1, num2):
    return num1 % num2
